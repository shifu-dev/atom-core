#include "atom/core.h"

namespace atom
{
    /// --------------------------------------------------------------------------------------------
    ///
    /// --------------------------------------------------------------------------------------------
    template <typename... ts>
    union union_of;

    /// --------------------------------------------------------------------------------------------
    ///
    /// --------------------------------------------------------------------------------------------
    template <typename type, typename... ts>
    union union_of<type, ts...>
    {
    public:
        type value;
        union_of<ts...> next;
    };

    /// --------------------------------------------------------------------------------------------
    ///
    /// --------------------------------------------------------------------------------------------
    template <>
    union union_of<>
    {};
}
