#include "Atom/Core.h"

namespace Atom
{
    /// --------------------------------------------------------------------------------------------
    ///
    /// --------------------------------------------------------------------------------------------
    template <typename... Ts>
    union UnionOf;

    /// --------------------------------------------------------------------------------------------
    ///
    /// --------------------------------------------------------------------------------------------
    template <typename T, typename... Ts>
    union UnionOf<T, Ts...>
    {
    public:
        T value;
        UnionOf<Ts...> next;
    };

    /// --------------------------------------------------------------------------------------------
    ///
    /// --------------------------------------------------------------------------------------------
    template <>
    union UnionOf<>
    {};
}
